#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t max_s1;
	size_t i;

	max_s1 = 0;
	i = 0;
	while (s1[max_s1] != '\0')
		max_s1++;
	while (s2[i] != '\0')
	{
		s1[max_s1] = s2[i];
		max_s1++;
		i++;
	}
	s1[max_s1] = '\0';
	return (s1);
}
