#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned char a;

	if (c < 0 || c > 255)
		return (0);
	a = c;
	if ((a >= 'A' && a <= 'Z') || (a >= 'a' && a <= 'z'))
		return (1);
	else
		return (0);
}
