#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	if (n > -2147483648 && n <= 2147483647)
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		if (n / 10 != 0)
			ft_putnbr(n / 10);
		c = n % 10 + '0';
		ft_putchar(c);
	}
	if (n == -2147483648)
	{
		ft_putchar('-');
		n = n / 10 * (-1);
		ft_putnbr(n / 10);
		c = n % 10 + '0';
		ft_putchar(c);
		ft_putchar('8');
	}
}
